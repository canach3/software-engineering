#include "LogoutUI.h"

LogoutUI::LogoutUI(Logout* logout) : logout(logout) {} // 생성자

/*
    함수 이름 : LogoutUI::selectLogout()
    기능 : input file의 로그아웃 관련 내용 읽고, 결과를 output file로 출력
    전달 인자 : in  -> 입력 파일 스트림, out -> 출력 파일 스트림
    반환값 : 없음
*/
void LogoutUI::selectLogout(ifstream& in, ofstream& out) {
    string id = logout->logoutUser();

    out << "2.2. 로그아웃 \n" << "> " << id << "\n";
    out << "\n";
}